class Chess_Man {
    constructor(name,color,value)
    {
        this.name = name;
        this.color = color;
        this.value = value;
    }
    getName(){
        return this.name;
    }
    getColor()
    {
        return this.color;
    }
    getValue()
    {
        return this.value;
    }
    setName()
    {
        this.name = name;
    }
    setColor()
    {
        this.color = color;
    }
    setValue()
    {
        this.value = value;
    }
}
